_project-cluster() {
  COMPREPLY=()
  local word="${COMP_WORDS[COMP_CWORD]}"

  if [ "$COMP_CWORD" -eq 1 ]; then
    COMPREPLY=( $(compgen -W "$(project-cluster commands)" -- "$word") )
  else
    local command="${COMP_WORDS[1]}"
    local completions="$(project-cluster completions "$command")"
    COMPREPLY=( $(compgen -W "$completions" -- "$word") )
  fi
}

complete -F _project-cluster project-cluster
